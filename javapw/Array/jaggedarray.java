public class jaggedarray {
    public static void main(String[] args) {
        int arr[][] = {
            { 2, 3 ,4,5}, { 4, 5 }, { 6, 7, 4                                                                   }
        };

    
    for(int i=0;i<=2;i++){
        for(int j=0;j<arr[i].length;j++){
            System.out.print(arr[i][j]+" ");

        }
        System.out.println(" ");

    }
        
    }
    
}
